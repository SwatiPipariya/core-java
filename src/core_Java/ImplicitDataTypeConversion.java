package core_Java;

public class ImplicitDataTypeConversion {
    
	public static void main(String[] args) {
		int a = 40;
		System.out.println("Int Representation:" +a);
		
		long b = a;
		System.out.println("Long Representation:" +b);
		
		float c = b;
		System.out.println("Float Representation:" +c);
		
	}
}
